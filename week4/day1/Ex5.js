function convertToGrams(kg) {
    return kg * 1000;  // Conversion en grammes
}

console.log(convertToGrams(2));  // Affichera 2000


const convertToGrams = function(kg) {
    return kg * 1000;  // Conversion en grammes
};

console.log(convertToGrams(2));  // Affichera 2000


const convertToGrams = (kg) => kg * 1000;  // Fonction fléchée pour la conversion
console.log(convertToGrams(2));  // Affichera 2000


